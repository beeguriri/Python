# 문자열의 모든 알파벳을 대문자로 변환한 새로운 문자열을 반환합니다.
print("hello, world!".upper())  # "HELLO, WORLD!"

# 문자열의 모든 알파벳을 소문자로 변환한 새로운 문자열을 반환합니다.
print("HeLLo, wOrLd!".lower())  # "hello, world!"

# 문자열의 첫 문자를 대문자로, 나머지 문자를 소문자로 변환한 새로운 문자열을 반환합니다.
print("hello, world!".capitalize())  # "Hello, world!"

# 문자열에서 단어의 첫 문자를 대문자로, 나머지 문자를 소문자로 변환한 새로운 문자열을 반환합니다.
print("hello, world!".title())  # "Hello, World!"

# 문자열에서 대문자는 소문자로, 소문자는 대문자로 변환한 새로운 문자열을 반환합니다.
print("Hello, World!".swapcase())  # "hELLO, wORLD!"