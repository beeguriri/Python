# for문사용

# 리스트의 원소를 순차적으로 출력
fruits = ["apple", "banana", "cherry"]

for elm in fruits:
    print(elm)

# 리스트에 저장된 모든 수의 합을 구하는 코드
numbers = [1, 2, 3, 4, 5]
sum = 0
for num in numbers:
    sum += num
print(sum)


# 튜플의 원소를 순차적으로 출력하는 예제
colors = ("red", "green", "blue")

for elm in colors:
    print(elm)
    
# 문자열의 각 문자를 순차적으로 출력하는 예제
text = "Python"

for c in text:
    print(c)

